export function getProductsByBrand(brand: Brand): Product[] {
  return products.filter((p) => p.brand === brand);
}

export function getFeaturedProducts(): Product[] {
  return products.filter((p) => p.isFeatured);
}

export function getProductBySlug(brand: Brand, slug: string): Product | undefined {
  return products.find((p) => p.brand === brand && p.slug === slug);
}

export function getAllSlugs(): Array<{ marca: string; slug: string }> {
  return products.map((p) => ({ marca: p.brand, slug: p.slug }));
}

